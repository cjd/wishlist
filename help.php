<h2 class="categoryHeader">Viewing Other People's Lists</h2>
<a name="viewOther"></a><h3>How do I view someone's list?</h3>
<p>Once you have been granted access to someone's wishlist, their name will appear on the homepage. To view their list, simply click on their name.</p>
<p>If you don't see the person's list you're looking for, you can request access by going to <b>Update Your Account >> Manage List Access</b> and searching for them by their username or email.</p>

<a name="checkOff"></a><h3>How do I mark an item as purchased?</h3>
<p>When viewing a list, you can mark items as purchased. If the person wants only one of an item, you'll see a checkbox. If they want multiple, you'll see a dropdown menu to select the quantity you've purchased.</p>
<p>After making your selections, click the <b>Save Changes</b> button at the bottom of the page. This will update the list for everyone else to see.</p>

<a name="removePurchase"></a><h3>How do I undo a purchase I marked?</h3>
<p>At the bottom of each wishlist, there is a link to the <b>Purchase Log</b>. Click this link to see a list of all items you have marked as purchased. Next to each item, you will see a <font color=red>[Remove Purchase]</font> link. Click this to undo the purchase.</p>

<a name="addComment"></a><h3>How do I add comments?</h3>
<p>At the bottom of each list, there is a <b>Comments</b> section. The owner of the list cannot see these comments, so you can use this space to coordinate with others viewing the list. Click the <b>Add New Comment</b> button to add your thoughts.</p>

